/**
 * Normalize residence to ISO code.
 * @param {string|null|undefined} str
 * @returns {string|null}
 */
export function parseResidence(str) {
  if (!str || typeof str !== "string") return null;
  const trimmed = str.trim();
  if (
    trimmed === "" ||
    trimmed === "Unknown" ||
    trimmed === "????" ||
    trimmed === "#N/A"
  )
    return null;
  // Skip values that look like time data (column shift artifacts)
  if (/^\d+:\d+:\d+$/.test(trimmed)) return null;
  // Skip gender values leaked into residence column
  if (trimmed === "男" || trimmed === "女") return null;
  // Skip organization names
  if (trimmed.startsWith("学連")) return null;

  // Japanese prefecture
  if (PREFECTURES[trimmed]) return PREFECTURES[trimmed];

  // City → prefecture
  if (CITY_TO_PREFECTURE[trimmed]) return CITY_TO_PREFECTURE[trimmed];

  // Country (case-insensitive; toLowerCase is no-op on CJK, so Japanese keys also match)
  const lower = trimmed.toLowerCase();
  if (COUNTRIES[lower]) return COUNTRIES[lower];

  return null;
}
